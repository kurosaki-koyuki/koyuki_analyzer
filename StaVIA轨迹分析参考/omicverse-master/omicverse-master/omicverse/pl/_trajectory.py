def _paga_tree_layout(
    adata,
    *,
    basis,
    groups,
    pseudotime,
    x,
    y,
    jitter_width,
):
    graph_data = _paga_graph_data(adata, basis=basis, groups=groups, x=x, y=y)
    paga = adata.uns["paga"]
    groups = groups or paga.get("groups")
    labels = pd.Series(adata.obs[groups].values, index=adata.obs_names)
    if isinstance(labels.dtype, pd.CategoricalDtype):
        categories = list(labels.dtype.categories)
    else:
        categories = list(pd.unique(labels))

    pseudotime_values = pd.to_numeric(
        adata.obs[pseudotime], errors="coerce"
    ).to_numpy(dtype=float)
    basis_cell_coords = graph_data.cell_coords.copy()
    basis_node_coords = graph_data.node_coords.copy()
    node_coords = basis_node_coords.copy()
    for idx, category in enumerate(categories):
        mask = np.asarray(labels == category)
        if mask.any():
            node_coords[idx, 1] = np.nanmedian(pseudotime_values[mask])
        else:
            node_coords[idx, 1] = np.nan

    n_nodes = len(categories)
    adj = [[] for _ in range(n_nodes)]
    for i, j in graph_data.edges:
        adj[i].append(j)
        adj[j].append(i)

    finite_times = np.where(np.isfinite(node_coords[:, 1]), node_coords[:, 1], np.inf)
    root_order = list(np.argsort(finite_times))
    parent = np.full(n_nodes, -1, dtype=int)
    children = [[] for _ in range(n_nodes)]
    visited = np.zeros(n_nodes, dtype=bool)
    for start in root_order:
        if visited[start]:
            continue
        queue = [int(start)]
        visited[start] = True
        while queue:
            node = queue.pop(0)
            neighbors = [neighbor for neighbor in adj[node] if not visited[neighbor]]
            neighbors = sorted(
                neighbors,
                key=lambda idx: (
                    finite_times[idx],
                    graph_data.node_coords[idx, 0],
                    graph_data.node_coords[idx, 1],
                ),
            )
            for neighbor in neighbors:
                parent[neighbor] = node
                children[node].append(neighbor)
                visited[neighbor] = True
                queue.append(neighbor)

    x_positions = np.full(n_nodes, np.nan, dtype=float)
    next_leaf = 0

    def assign_x(node):
        nonlocal next_leaf
        if not children[node]:
            x_positions[node] = float(next_leaf)
            next_leaf += 1
        else:
            for child in children[node]:
                assign_x(child)
            x_positions[node] = float(np.nanmean(x_positions[children[node]]))

    for start in root_order:
        if np.isnan(x_positions[start]):
            assign_x(int(start))
            next_leaf += 1

    if np.all(np.isfinite(x_positions)):
        x_positions -= np.nanmean(x_positions)
        x_positions *= 1.65
        basis_x = basis_node_coords[:, 0].astype(float)
        basis_span = np.nanmax(basis_x) - np.nanmin(basis_x)
        tree_span = np.nanmax(x_positions) - np.nanmin(x_positions)
        if basis_span > 1e-8 and tree_span > 1e-8:
            basis_scaled = (basis_x - np.nanmean(basis_x)) / basis_span * tree_span
            corr = np.corrcoef(
                x_positions[np.isfinite(x_positions) & np.isfinite(basis_scaled)],
                basis_scaled[np.isfinite(x_positions) & np.isfinite(basis_scaled)],
            )[0, 1]
            if np.isfinite(corr) and corr < 0:
                basis_scaled *= -1
            x_positions = 0.64 * x_positions + 0.36 * basis_scaled
    else:
        x_positions = graph_data.node_coords[:, 0].copy()
    node_coords[:, 0] = x_positions

    category_to_idx = {category: idx for idx, category in enumerate(categories)}
    cell_x = np.zeros(adata.n_obs, dtype=float)
    dx = np.nanmax(node_coords[:, 0]) - np.nanmin(node_coords[:, 0])
    dx = dx if dx > 0 else 1.0
    lane_width = max(0.34, min(0.85, dx * 0.22))
    for obs_idx, value in enumerate(labels):
        node = category_to_idx.get(value)
        if node is None:
            cell_x[obs_idx] = np.nan
            continue
        par = parent[node]
        if par >= 0 and np.isfinite(node_coords[par, 1]) and np.isfinite(node_coords[node, 1]):
            start_time = node_coords[par, 1]
            end_time = node_coords[node, 1]
            span = end_time - start_time
            if abs(span) > 1e-8:
                frac = np.clip((pseudotime_values[obs_idx] - start_time) / span, 0.0, 1.0)
                cell_x[obs_idx] = node_coords[par, 0] + frac * (
                    node_coords[node, 0] - node_coords[par, 0]
                )
            else:
                cell_x[obs_idx] = node_coords[node, 0]
        else:
            cell_x[obs_idx] = node_coords[node, 0]

    for category in categories:
        node = category_to_idx[category]
        mask = np.asarray(labels == category)
        if not mask.any():
            continue
        par = parent[node]
        if par >= 0:
            direction = basis_node_coords[node] - basis_node_coords[par]
        elif children[node]:
            direction = basis_node_coords[children[node][0]] - basis_node_coords[node]
        else:
            direction = np.array([1.0, 0.0])
        if np.linalg.norm(direction) > 1e-8:
            direction = direction / np.linalg.norm(direction)
            axis = np.array([-direction[1], direction[0]])
        else:
            axis = np.array([1.0, 0.0])
        center = np.nanmedian(basis_cell_coords[mask], axis=0)
        score = (basis_cell_coords[mask] - center) @ axis
        finite_score = score[np.isfinite(score)]
        scale = (
            np.nanpercentile(np.abs(finite_score), 90)
            if len(finite_score)
            else 0.0
        )
        if not np.isfinite(scale) or scale <= 1e-8:
            scale = np.nanstd(finite_score) if len(finite_score) else 0.0
        if not np.isfinite(scale) or scale <= 1e-8:
            continue
        lateral = np.clip(score / scale, -1.0, 1.0) * lane_width
        cell_x[mask] += lateral

    coords_scatter = np.column_stack([cell_x, pseudotime_values])
    rng = np.random.default_rng(0)
    coords_scatter[:, 0] += rng.normal(
        0.0,
        max(dx * jitter_width, 0.026),
        size=coords_scatter.shape[0],
    )

    graph_data.node_coords = node_coords
    directed_edges = []
    edge_weights = graph_data.edge_weights or [1.0] * len(graph_data.edges)
    for (i, j), weight in zip(graph_data.edges, edge_weights):
        time_i = node_coords[i, 1]
        time_j = node_coords[j, 1]
        if np.isfinite(time_i) and np.isfinite(time_j) and time_i <= time_j:
            directed_edges.append((i, j, float(weight)))
        elif np.isfinite(time_i) and np.isfinite(time_j):
            directed_edges.append((j, i, float(weight)))
        elif parent[j] == i:
            directed_edges.append((i, j, float(weight)))
        elif parent[i] == j:
            directed_edges.append((j, i, float(weight)))
    graph_data.directed_edges = directed_edges
    return graph_data, coords_scatter


def _stavia_tree_layout(
    data,
    *,
    model=None,
    basis=None,
    cluster_key=None,
    key="stavia",
    pseudotime="Pseudotime",
    x=0,
    y=1,
    jitter_width=0.006,
):
    context = _stavia_plot_context(
        data,
        model=model,
        basis=basis,
        cluster_key=cluster_key,
        key=key,
        x=x,
        y=y,
    )
    adata = context["adata"]
    raw_model = context["raw_model"]
    labels = context["labels"]
    edges = context["edges"]
    terminals = context["terminals"]
    roots = context["roots"]
    clusters = context["clusters"]
    embedding_coords = context["embedding_coords"]
    centers = context["centers"]
    value_map = context["value_map"]

    pseudotime_values, pseudotime_label = _resolve_stavia_pseudotime(
        data,
        adata,
        raw_model,
        key=key,
        pseudotime=pseudotime,
    )

    label_array = np.asarray(labels, dtype=object)
    cluster_time = {}
    for cluster in clusters:
        mask = label_array == cluster
        if np.any(mask):
            cluster_time[cluster] = float(np.nanmedian(pseudotime_values[mask]))
        else:
            cluster_time[cluster] = float(value_map.get(cluster, np.nan))

    cluster_to_idx = {cluster: idx for idx, cluster in enumerate(clusters)}
    n_nodes = len(clusters)
    adjacency = {cluster: [] for cluster in clusters}
    edge_clusters = []
    for start, end in edges:
        if start not in cluster_to_idx or end not in cluster_to_idx:
            continue
        adjacency[start].append(end)
        adjacency[end].append(start)
        edge_clusters.append((start, end))

    finite_default = np.inf
    root_order = [root for root in roots if root in cluster_to_idx]
    remaining = [
        cluster for cluster in clusters
        if cluster not in root_order
    ]
    remaining = sorted(
        remaining,
        key=lambda cluster: (
            cluster_time.get(cluster, finite_default)
            if np.isfinite(cluster_time.get(cluster, np.nan))
            else finite_default,
            _cluster_sort_key(cluster),
        ),
    )
    root_order.extend(remaining)

    parent = {cluster: None for cluster in clusters}
    children = {cluster: [] for cluster in clusters}
    visited = set()
    for start in root_order:
        if start in visited:
            continue
        queue = [start]
        visited.add(start)
        while queue:
            node = queue.pop(0)
            neighbors = [neighbor for neighbor in adjacency[node] if neighbor not in visited]
            neighbors = sorted(
                neighbors,
                key=lambda cluster: (
                    cluster_time.get(cluster, finite_default)
                    if np.isfinite(cluster_time.get(cluster, np.nan))
                    else finite_default,
                    _cluster_sort_key(cluster),
                ),
            )
            for neighbor in neighbors:
                parent[neighbor] = node
                children[node].append(neighbor)
                visited.add(neighbor)
                queue.append(neighbor)

    x_positions = {}
    next_leaf = 0

    def assign_x(cluster):
        nonlocal next_leaf
        if cluster in x_positions:
            return x_positions[cluster]
        if not children[cluster]:
            x_positions[cluster] = float(next_leaf)
            next_leaf += 1
        else:
            child_x = [assign_x(child) for child in children[cluster]]
            x_positions[cluster] = float(np.nanmean(child_x))
        return x_positions[cluster]

    for start in root_order:
        if start not in x_positions:
            assign_x(start)
            next_leaf += 1

    node_coords = np.zeros((n_nodes, 2), dtype=float)
    for cluster, idx in cluster_to_idx.items():
        node_coords[idx, 0] = x_positions.get(cluster, float(idx))
        node_coords[idx, 1] = cluster_time.get(cluster, np.nan)

    if n_nodes > 1 and np.all(np.isfinite(node_coords[:, 0])):
        node_coords[:, 0] -= np.nanmean(node_coords[:, 0])
        node_coords[:, 0] *= 1.65
        basis_x = np.asarray(
            [
                centers.get(cluster, np.array([np.nan, np.nan], dtype=float))[0]
                for cluster in clusters
            ],
            dtype=float,
        )
        valid = np.isfinite(basis_x) & np.isfinite(node_coords[:, 0])
        if np.sum(valid) > 1:
            basis_span = np.nanmax(basis_x[valid]) - np.nanmin(basis_x[valid])
            tree_span = np.nanmax(node_coords[valid, 0]) - np.nanmin(node_coords[valid, 0])
            if basis_span > 1e-8 and tree_span > 1e-8:
                basis_scaled = (basis_x - np.nanmean(basis_x[valid])) / basis_span * tree_span
                corr = np.corrcoef(node_coords[valid, 0], basis_scaled[valid])[0, 1]
                if np.isfinite(corr) and corr < 0:
                    basis_scaled *= -1
                node_coords[valid, 0] = 0.64 * node_coords[valid, 0] + 0.36 * basis_scaled[valid]

    cell_x = np.zeros(adata.n_obs, dtype=float)
    dx = np.nanmax(node_coords[:, 0]) - np.nanmin(node_coords[:, 0]) if n_nodes else 1.0
    dx = dx if dx > 0 else 1.0
    lane_width = max(0.34, min(0.85, dx * 0.22))
    for obs_idx, cluster in enumerate(label_array):
        node_idx = cluster_to_idx.get(cluster)
        if node_idx is None:
            cell_x[obs_idx] = np.nan
            continue
        parent_cluster = parent.get(cluster)
        if parent_cluster is not None:
            parent_idx = cluster_to_idx[parent_cluster]
            start_time = node_coords[parent_idx, 1]
            end_time = node_coords[node_idx, 1]
            span = end_time - start_time
            if np.isfinite(span) and abs(span) > 1e-8:
                frac = np.clip((pseudotime_values[obs_idx] - start_time) / span, 0.0, 1.0)
                cell_x[obs_idx] = node_coords[parent_idx, 0] + frac * (
                    node_coords[node_idx, 0] - node_coords[parent_idx, 0]
                )
            else:
                cell_x[obs_idx] = node_coords[node_idx, 0]
        else:
            cell_x[obs_idx] = node_coords[node_idx, 0]

    for cluster in clusters:
        mask = label_array == cluster
        if not np.any(mask):
            continue
        parent_cluster = parent.get(cluster)
        if parent_cluster is not None and cluster in centers and parent_cluster in centers:
            direction = np.asarray(centers[cluster]) - np.asarray(centers[parent_cluster])
        elif children[cluster] and cluster in centers and children[cluster][0] in centers:
            direction = np.asarray(centers[children[cluster][0]]) - np.asarray(centers[cluster])
        else:
            direction = np.array([1.0, 0.0])
        if np.linalg.norm(direction) > 1e-8:
            direction = direction / np.linalg.norm(direction)
            axis = np.array([-direction[1], direction[0]])
        else:
            axis = np.array([1.0, 0.0])
        center = np.nanmedian(embedding_coords[mask], axis=0)
        score = (embedding_coords[mask] - center) @ axis
        finite_score = score[np.isfinite(score)]
        scale = (
            np.nanpercentile(np.abs(finite_score), 90)
            if len(finite_score)
            else 0.0
        )
        if not np.isfinite(scale) or scale <= 1e-8:
            scale = np.nanstd(finite_score) if len(finite_score) else 0.0
        if not np.isfinite(scale) or scale <= 1e-8:
            continue
        cell_x[mask] += np.clip(score / scale, -1.0, 1.0) * lane_width

    coords_scatter = np.column_stack([cell_x, pseudotime_values])
    rng = np.random.default_rng(0)
    coords_scatter[:, 0] += rng.normal(
        0.0,
        max(dx * jitter_width, 0.026),
        size=coords_scatter.shape[0],
    )

    edge_indices = [
        (cluster_to_idx[start], cluster_to_idx[end])
        for start, end in edge_clusters
    ]
    directed_edges = []
    for i, j in edge_indices:
        time_i = node_coords[i, 1]
        time_j = node_coords[j, 1]
        if np.isfinite(time_i) and np.isfinite(time_j) and time_i <= time_j:
            directed_edges.append((i, j, 1.0))
        elif np.isfinite(time_i) and np.isfinite(time_j):
            directed_edges.append((j, i, 1.0))

    terminal_indices = [
        cluster_to_idx[terminal]
        for terminal in terminals
        if terminal in cluster_to_idx
    ]
    terminal_labels = [
        f"TS{terminal}"
        for terminal in terminals
        if terminal in cluster_to_idx
    ]
    graph_data = _GraphData(
        node_coords=node_coords,
        edges=edge_indices,
        branch_indices=terminal_indices,
        branch_labels=terminal_labels,
        cell_coords=coords_scatter,
        edge_weights=[1.0] * len(edge_indices),
        directed_edges=directed_edges,
    )
    return graph_data, coords_scatter, pseudotime_label, context


@register_function(
    aliases=[
        "trajectory_tree",
        "trajectory tree",
        "pseudotime tree",
        "轨迹树",
        "伪时间树图",
    ],
    category="pl",
    description=(
        "Draw a tree-layout trajectory view with real pseudotime on the "
        "y-axis."
    ),
    examples=[
        (
            "ov.pl.trajectory_tree(mono.adata, method='monocle', "
            "pseudotime='Pseudotime', color='State')"
        ),
        (
            "ov.pl.trajectory_tree(adata, method='paga', basis='X_umap', "
            "groups='clusters', pseudotime='dpt_pseudotime', "
            "color='clusters')"
        ),
    ],
    related=[
        "pl.trajectory",
        "pl.trajectory_overlay",
        "pl.branch_streamplot",
    ],
)
def trajectory_tree(
    adata,
    *,
    color=None,
    color_by=None,
    method="auto",
    model=None,
    basis="X_umap",
    groups=None,
    cluster_key=None,
    key="stavia",
    pseudotime="Pseudotime",
    x=0,
    y=1,
    show_branch_points=True,
    cell_size=2.0,
    cell_link_size=0.7,
    figsize=(5, 4),
    cmap=None,
    ax=None,
    legend_loc="right margin",
    legend_fontsize=None,
    cell_alpha=0.88,
    jitter_width=0.006,
    save=None,
    dpi=150,
    show=None,
):
    """Draw a tree-layout trajectory view using real pseudotime on the y-axis.

    The tree view separates branches horizontally while preserving the real
    pseudotime values on the vertical axis. The y-axis is inverted so smaller
    pseudotime values are shown at the top. Monocle uses the cell projection
    MST, while PAGA uses a tree-like layout derived from the PAGA graph and
    draws directed arrows from lower to higher pseudotime.

    Parameters
    ----------
    adata
        Annotated data matrix containing trajectory and pseudotime results.
    color
        Observation key used for coloring cells. If omitted, ``"State"`` is
        used when present.
    color_by
        Backward-compatible alias for ``color``.
    method
        Trajectory backend. ``trajectory_tree`` currently supports
        ``"monocle"``, ``"paga"``, and ``"stavia"``.
    basis
        Low-dimensional representation used to initialize PAGA branch
        separation. Monocle tree layout does not use this parameter.
    groups
        Observation key defining PAGA groups. When omitted, the key stored in
        ``adata.uns['paga']['groups']`` is used.
    pseudotime
        Key in ``adata.obs`` containing pseudotime values for the y-axis.
    show_branch_points
        Whether to label branch nodes when branch points can be inferred.
    cell_size
        Base cell marker size.
    cell_link_size
        Line width for tree edges.
    figsize
        Figure size used when ``ax`` is not provided.
    cmap
        Colormap used for continuous cell colors.
    ax
        Existing matplotlib axes. When omitted, a new figure is created.
    legend_loc
        Legend location for categorical cell colors.
    legend_fontsize
        Legend font size for categorical cell colors.
    cell_alpha
        Cell marker opacity.
    jitter_width
        Horizontal jitter applied to cells for readability.
    save
        Optional output path passed to ``Figure.savefig``.
    dpi
        Resolution used when saving the figure.
    show
        Whether to call :func:`matplotlib.pyplot.show`.

    Returns
    -------
    tuple[matplotlib.figure.Figure, matplotlib.axes.Axes]
        Figure and axes containing the pseudotime tree view.
    """
    requested_method = "auto" if method is None else str(method).lower()
    if requested_method == "auto":
        if _is_stavia_result(adata, model=model, key=key):
            method = "stavia"
        else:
            method = _normalize_method(adata, method="auto", model=model)
    elif requested_method in {"stavia", "via"}:
        method = "stavia"
    else:
        method = _normalize_method(adata, method=requested_method, model=model)

    plot_adata = adata.adata if _is_fitted_trajectory_wrapper(adata) else adata
    color = color if color is not None else color_by
    if color is None and method == "stavia":
        summary = getattr(plot_adata, "uns", {}).get(key, {})
        color = cluster_key or summary.get("cluster_key")
    if color is None:
        color = "State" if "State" in plot_adata.obs else None

    if method != "stavia" and pseudotime not in plot_adata.obs:
        raise KeyError(f"`{pseudotime}` was not found in `adata.obs`.")

    if ax is None:
        fig, ax = plt.subplots(1, 1, figsize=figsize, dpi=dpi)
    else:
        fig = ax.figure

    tree_cell_size = cell_size
    tree_cell_alpha = cell_alpha
    if method == "paga":
        if cell_size == 2.0:
            tree_cell_size = 1.0
        if cell_alpha == 0.88:
            tree_cell_alpha = 0.52

    branch_anchors = None
    branch_labels = None
    branch_avoid_coords = None
    tree_graph_data = None
    if method == "monocle":
        monocle = plot_adata.uns["monocle"]
        if "pr_graph_cell_proj_tree" not in monocle:
            raise ValueError("Run order_cells() first to build the cell projection MST.")

        cell_mst = monocle["pr_graph_cell_proj_tree"]
        vertex_names = cell_mst.vs["name"]
        pseudotime_values = plot_adata.obs.loc[vertex_names, pseudotime].to_numpy(dtype=float)
        root_idx = int(np.nanargmin(pseudotime_values))
        coords = np.asarray(cell_mst.layout_reingold_tilford(root=[root_idx]).coords)
        coords[:, 1] = pseudotime_values

        name_to_idx = {name: idx for idx, name in enumerate(vertex_names)}
        order = [name_to_idx[name] for name in plot_adata.obs_names]
        coords_sorted = coords[order]
        dx = np.nanmax(coords[:, 0]) - np.nanmin(coords[:, 0])
        dx = dx if dx > 0 else 1.0
        rng = np.random.default_rng(0)
        coords_scatter = coords_sorted.copy()
        coords_scatter[:, 0] += rng.normal(
            0.0, dx * jitter_width, size=coords_scatter.shape[0]
        )

        for edge in cell_mst.es:
            i, j = edge.source, edge.target
            ax.plot(
                [coords[i, 0], coords[j, 0]],
                [coords[i, 1], coords[j, 1]],
                color="#8A8A8A",
                linewidth=cell_link_size,
                alpha=0.68,
                zorder=1,
            )

        if show_branch_points:
            branch_anchors, branch_labels = _complex_branch_point_anchors(
                monocle, coords_sorted
            )
            branch_avoid_coords = coords_scatter
    elif method == "paga":
        tree_graph_data, coords_scatter = _paga_tree_layout(
            plot_adata,
            basis=basis,
            groups=groups,
            pseudotime=pseudotime,
            x=x,
            y=y,
            jitter_width=jitter_width,
        )
        if show_branch_points and tree_graph_data.branch_indices:
            branch_anchors = tree_graph_data.node_coords[tree_graph_data.branch_indices]
            branch_labels = tree_graph_data.branch_labels
            branch_avoid_coords = coords_scatter
    elif method == "stavia":
        tree_graph_data, coords_scatter, pseudotime, context = _stavia_tree_layout(
            adata,
            model=model,
            basis=basis,
            cluster_key=cluster_key,
            key=key,
            pseudotime=pseudotime,
            x=x,
            y=y,
            jitter_width=jitter_width,
        )
        plot_adata = context["adata"]
        if color is None:
            color = context["cluster_key"]
        if show_branch_points and tree_graph_data.branch_indices:
            branch_anchors = tree_graph_data.node_coords[tree_graph_data.branch_indices]
            branch_labels = tree_graph_data.branch_labels
            branch_avoid_coords = coords_scatter
    else:
        raise NotImplementedError(
            f"`trajectory_tree` does not support method={method!r}."
        )

    _draw_tree_cells(
        ax,
        plot_adata,
        coords_scatter,
        color=color,
        cell_size=tree_cell_size,
        cell_alpha=tree_cell_alpha,
        cmap=cmap,
        legend_loc=legend_loc,
        legend_fontsize=legend_fontsize,
    )
    if tree_graph_data is not None and not ax.yaxis_inverted():
        ax.invert_yaxis()
    if tree_graph_data is not None:
        _draw_graph(
            ax,
            tree_graph_data,
            show_tree=True,
            show_branch_points=False,
            backbone_color="#1A1A1A",
            cell_link_size=max(cell_link_size, 1.2),
            branch_point_size=150,
            branch_point_color="#6F6F6F",
            branch_point_label_color="white",
            branch_point_label_fontsize=10,
            tree_alpha=0.82,
            zorder_base=3,
            arrow=True,
        )
    if branch_anchors is not None:
        branch_label_positions = _draw_branch_points(
            ax,
            branch_anchors,
            branch_labels,
            size=150,
            facecolor="#6F6F6F",
            fontsize=10,
            alpha=0.92,
            zorder=5,
            avoid_coords=branch_avoid_coords,
        )
        _expand_limits_to_points(
            ax,
            np.vstack([np.asarray(branch_anchors), branch_label_positions]),
            frac=0.06,
        )

    if not ax.yaxis_inverted():
        ax.invert_yaxis()
    ax.set_xlabel("")
    ax.set_ylabel(pseudotime, labelpad=10)
    ax.set_xticks([])
    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)
    ax.spines["bottom"].set_visible(False)
    ax.tick_params(axis="x", bottom=False)
    fig.tight_layout(rect=(0, 0, 0.86, 1) if legend_loc in {"right margin", "right"} else None)
    if save:
        fig.savefig(save, dpi=dpi, bbox_inches="tight")
    if show:
        plt.show()
    return fig, ax
